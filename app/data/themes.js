export function getRandomTheme() {
  const themeKeys = Object.keys(themes)
  const randomKey = themeKeys[Math.floor(Math.random() * themeKeys.length)]
  return randomKey
}

export function validateTheme(themeName) {
  return themes.hasOwnProperty(themeName) ? themeName : "animals"
}

// Custom deck support (for future implementation)
export function createCustomDeck(images) {
  return {
    name: "Custom",
    preview: "🎨",
    backColor: "from-gray-500 to-gray-700",
    cards: images.slice(0, 20), // Limit to 20 custom images
  }
}
